#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	unsigned int	b;

	b = 0;
	if (n < 0)
	{
		ft_putchar_fd('-', fd);
		b = -n;
	}
	else
		b = n;
	if (b >= 10)
	{
		ft_putnbr_fd(b / 10, fd);
		ft_putnbr_fd(b % 10, fd);
	}
	else
		ft_putchar_fd(b + '0', fd);
}
