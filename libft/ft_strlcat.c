#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t size)
{
	size_t i;
	size_t j;
	size_t res;

	j = 0;
	res = 0;
	i = ft_strlen(dst);
	res = ft_strlen(src);
	if (size <= i)
		return (res + size);
	else
		res = res + i;
	while (src[j] && i + 1 < size)
	{
		dst[i] = src[j];
		i++;
		j++;
	}
	dst[i] = '\0';
	return (res);
}
