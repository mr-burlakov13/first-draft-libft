#include "libft.h"

void	ft_putnbr(int a)
{
	unsigned int	b;
	char			c;

	c = '0';
	if (a < 0)
	{
		write(1, "-", 1);
		b = -a;
	}
	else
		b = a;
	if (b >= 10)
	{
		ft_putnbr(b / 10);
		ft_putnbr(b % 10);
	}
	else
	{
		c = c + b;
		write(1, &c, 1);
	}
}
